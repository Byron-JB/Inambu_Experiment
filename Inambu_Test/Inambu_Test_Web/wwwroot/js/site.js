window.setCookieAndReload = function (name, value, days) {
    let expires = "";
    if (days) {
        const date = new Date();
        date.setTime(date.getTime() + (days * 24 * 60 * 60 * 1000));
        expires = "; expires=" + date.toUTCString();
    }

    console.log("Attempting to write to cookie "+ name)
    document.cookie = `${name}=${value}${expires}; path=/`;
    location.reload();
};
